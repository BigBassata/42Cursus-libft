#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int i;
	int num;
	int sign;

	i = 0;
	num = 0;
	sign = 1;
	while ((nptr[i] >= 9 && nptr[i] <= 13) || nptr[i] == 32)
		i++;
	if (nptr[i] == '-' || nptr[i] == '+')
	{
		if (nptr[i] == '-')
			sign = -sign;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		num = (nptr[i] - '0') + 10 * num;
		i++;
	}
	return (num * sign);
}
